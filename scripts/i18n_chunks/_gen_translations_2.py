def main():
    chunk_path = Path(__file__).parent / "chunk_2.json"
    out_path = Path(__file__).parent / "translations_2.json"
    chunk = json.loads(chunk_path.read_text(encoding="utf-8"))
    missing = [k for k in chunk if k not in T]
    extra = [k for k in T if k not in chunk]
    if missing:
        raise SystemExit(f"Missing translations for {len(missing)} strings: {missing[:5]}...")
    if extra:
        raise SystemExit(f"Extra translations not in chunk: {extra[:5]}...")
    ordered = {}
    for key in chunk:
        entry = {"en": key, **T[key]}
        missing_locales = [loc for loc in LOCALES if loc not in entry]
        if missing_locales:
            raise SystemExit(f"Missing locales {missing_locales} for: {key!r}")
        ordered[key] = entry
    out_path.write_text(json.dumps(ordered, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(f"Wrote {len(ordered)} strings to {out_path}")
    print(f"Validation: len={len(ordered)}, keys_match={set(ordered)==set(chunk)}")
    sample = next(iter(ordered.values()))
    print(f"Locales per entry: {len(sample)} ({', '.join(sample.keys())})")


if __name__ == "__main__":
    main()
